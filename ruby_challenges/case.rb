current_weather = 'cold'

case current_weather
when 'sunny'
    puts "Suns out guns out!"
when 'rainy'
    puts "Umbrella up like poppins!"
when 'cold'
    puts "Layer up like Ralphie!"
when 'foggy'
    puts "Keep your eyes wide, its low vis"
else 
    puts "Look out the window, I have no clue what the weather is like..."
end